package joachimrussig.heatstressrouting.optimaltime.finder;

public class OptimalTimeFinderException extends RuntimeException {

	private static final long serialVersionUID = 4957358872792485630L;

	public OptimalTimeFinderException() {
		super();
	}

	public OptimalTimeFinderException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OptimalTimeFinderException(String message, Throwable cause) {
		super(message, cause);
	}

	public OptimalTimeFinderException(String message) {
		super(message);
	}

	public OptimalTimeFinderException(Throwable cause) {
		super(cause);
	}

	
	
}
